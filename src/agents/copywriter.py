DEFAULT_PROMPT = """
Напиши пост для Telegram-канала про AI-инструменты.

Тема: {topic}
Инструмент: {product_name}
Описание: {product_description}
Ссылка: {affiliate_link}
CTA: {cta}

Правила:
- Только русский язык
- Полезный контент с эмодзи
- CTA + ссылка в конце
- Длина: 250-400 символов
"""


def write_post(content_plan: Dict) -> Dict:
    """
    Пишет черновик поста по плану от Strategist.
    Выбирает промпт в зависимости от формата.
    """
    format_ = content_plan.get("format", "Обзор инструмента")
    prompt_template = PROMPTS.get(format_, DEFAULT_PROMPT)

    prompt = prompt_template.format(
        topic=content_plan["topic"],
        format=format_,
        product_name=content_plan["product"]["name"],
        product_description=content_plan["product"]["description"],
        affiliate_link=content_plan["product"]["affiliate_link"],
        cta=content_plan["cta"],
    )

    ai_result = generate_ai_text(prompt)
    draft_text = ai_result["text"].strip()

    # Убираем кавычки которые иногда добавляет AI
    if draft_text.startswith('"') and draft_text.endswith('"'):
        draft_text = draft_text[1:-1].strip()

    # Убираем markdown заголовки если AI их добавил
    lines = draft_text.split('\n')
    cleaned_lines = []
    for line in lines:
        if line.startswith('# ') or line.startswith('## '):
            line = line.lstrip('#').strip()
        cleaned_lines.append(line)
    draft_text = '\n'.join(cleaned_lines)

    return {
        "plan": content_plan,
        "draft_text": draft_text,
        "ai_source": ai_result["source"],
        "format": format_,
    }


